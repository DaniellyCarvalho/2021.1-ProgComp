# Atribuindo valor a uma variável
numero_x = 40
numero_y = 15

# Calculando e imprimindo a soma
soma = numero_x + numero_y
print('A soma de', numero_x, 'com', numero_y, 'é igual a', soma)

# Calculando e imprimindo a média
media = (numero_x + numero_y) / 2
print('A média de', numero_x, 'e', numero_y, 'é igual a', media)

# Calculando a divisão de X por Y
divisao = numero_x / numero_y
print('A divisão de', numero_x, 'por', numero_y, 'é igual a', divisao)

# Calculando a divisão INTEIRA de X por Y
divisao_inteira = numero_x // numero_y
print('A divisão INTEIRA de', numero_x, 'por', numero_y, 'é igual a', divisao_inteira)

# Calculando o RESTO da divisão INTEIRA de X por Y
resto = numero_x % numero_y
print('O RESTO da divisão INTEIRA de', numero_x, 'por', numero_y, 'é igual a', resto)

# Calculando o quadrado de numero_x
quadrado_x = numero_x ** 2
print('O quadrado de', numero_x, 'é igual a', quadrado_x)